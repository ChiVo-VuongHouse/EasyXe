package com.example.easyxe;

public class Product {
    int id;
    String name;
    Double price;
    String location;
    String seller;
    String image;

    public Product(int id, String name, Double price, String location, String seller, String image) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.location = location;
        this.seller = seller;
        this.image = image;
    }


}
